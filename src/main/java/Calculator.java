public class Calculator {


    /**
     * Hella coll method to add numbers
     * @param a
     * @param b
     * @return
     */
    public static int sum (int a, int b){
        return a+b;
    }


    /**
     * Cool new multiplayer feature
     * @param factor1
     * @param factor2
     * @return
     */
    public static int multiply (int factor1, int factor2){
        return factor1*factor2;
    }


}
